/**
* HalfDollar .50 value Coin Subclass.
* @author Lisa Miller
* @since 1/24/22
*/
public class HalfDollar extends Coin {

   /**
   * Constructor, needs no parameters.
   * Sends default HalfDollar values
   */
   public HalfDollar() {
      super(.50, "Half Dollar", "John F. Kennedy", "Presidential Coat of Arms");
   }
}
